                    <p>Karya tulis Ilmiah dapat dipahami sebagai teks yang ditulis dengan susunan sistematis, serta logis, dan memenuhi kaidah ilmu pengetahuan yang berlaku. Aspek rasionalitas diutamakan dalam penulisan karya tulis ilmiah, dan permasalahan yang diangkat bersifat objektif dan faktual. Karya tulis ilmiah menuntut untuk menggunakan kata-kata yang tidak ambigu atau bermakna ganda sehingga dibutuhkan gaya bahasayang lugas, serta eksplisit, dan tentunya dengan ragam ilmiah sesuai dengan PUEBI.</p>
                    <p>Karya Tulis Ilmiah (KTI) terbagi menjadi dua jenis. Pertama, KTI hasil penelitian dan pengembangan. Kedua, KTI berupa tinjauan, ulasan, kajian, atau hasil pemikiran. Kedua jenis KTI tersebut disusun dengan format penulisan yang sama. Perbedaan keduanya hanya pada tingkat kejelasan subjek yang tinjau dan dikaji, dengan tingkat yang tertinggi berada pada KTI yang berupa tinjauan, ulasan, kajian, atau hasil pemikiran. Subjek pada KTI jenis tinjauan, ulasan, kajian dan hasil pemikiran dijelaskan secara keseluruhan dan lengkap.</p>
                    <p><h1>Bentuk Karya Tulis Ilmiah : </h1></p>
                    <p>Buku ilmiah adalah rangkuman hasil-hasil penelitian terbaru yang disusun berdasarkan hasil kajian mendalam tentang masalah terkini dalam suatu bidang keilmuan serta berlandaskan pada kaidah, format, dan sistematika baku yang berlaku.</p>
                    <p>Bunga rampai adalah kumpulan KTI yang disusun dengan pendekatan dari beberapa sudat pandang bidang keilmuan yang memiliki tema yang sama. Bunga rampai tersusun dari bab-bab dengan pembahasan yang lengkap. Tiap bab terdiri dari bahasan yang dapat berbeda dengan bab lain, tetapi tetap memiliki keterkaitan satu sama lain. Pembahasan pada bunga rampai hampir sama dengan buku ilmiah. Pembeda utama antara bunga rampai dan buku ilmiah terletak pada konten prakata atau prolog dan penutup atau epilog. Prolog bunga rampai merangkum secara keseluruhan isi dalam tiap babnya, sedangkan epilog bunga rampai merupakan hasil analisis terhadap keseluruhan isi pada tiap babnya. Bunga rampai ditulis oleh para penulis secara bersama-sama. Seorang pakar ditunjuk untuk mengedit tiap bab yang telah ditulis.</p>
                    <p>Prosiding ilmiah adalah salah satu bentuk buku cetak atau salinan lunak yang berisi kumpulan makalah ilmiah yang diterbitkan mininal di tingkat nasional dan memiliki Nomor Buku Standar Internasional atau Nomor Serial Standar Internasional serta diedit oleh beberapa pakar keilmuan yang sesuai dengan tema bahasan. Sebelum dimasukkan ke dalam prosiding ilmiah, kumpulan makalah ilmiah tersebut telah diseleksi, dipaparkan, disunting, serta diatur tata letaknya sehingga menjadi satu kesatuan pubikasi yang utuh dan terjilid.</p>
